pub type Block = [u8; 16];

pub fn mul_init(x: &Block, y: &Block) -> Block
{
    let x: u128 = u128::from_be_bytes(*x);
    let y: u128 = u128::from_be_bytes(*y);
    let r: u128 = 0xE1000000000000000000000000000000;
    let mut z: u128 = 0;
    let mut v = x;

    for i in 0..128
    {
        if y & (1<<(127-i)) != 0 {z ^= v}
        v = (v >> 1) ^ if v & 1 == 0 {0} else {r}
    }
    z.to_be_bytes()
}

